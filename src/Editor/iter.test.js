import * as iter from "./iter"

test("backwards-rune", () => {
	expect(iter.rtl.rune("")).toBe("")
	expect(iter.rtl.rune("\n")).toBe("\n")
	expect(iter.rtl.rune("\n\n")).toBe("\n")
	expect(iter.rtl.rune("Hello")).toBe("o")
	expect(iter.rtl.rune("Hello,")).toBe(",")
	expect(iter.rtl.rune("Hello, ")).toBe(" ")
	expect(iter.rtl.rune("Hello, world")).toBe("d")
	expect(iter.rtl.rune("Hello, world!")).toBe("!")
	expect(iter.rtl.rune("Hello, world! ")).toBe(" ")
	expect(iter.rtl.rune("Hello, world! ðŸ˜€")).toBe("ðŸ˜€")
	expect(iter.rtl.rune("Hello, world! ðŸ˜€\n")).toBe("\n")
	expect(iter.rtl.rune("Hello, world! ðŸ˜€\n\n")).toBe("\n")
})

