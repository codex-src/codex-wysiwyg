test("forwards-rune", () => {
	expect(iter.ltr.rune("\n\nðŸ˜€ !dlrow ,olleH\n\n")).toBe("\n")
	expect(iter.ltr.rune("\nðŸ˜€ !dlrow ,olleH\n\n")).toBe("\n")
	expect(iter.ltr.rune("ðŸ˜€ !dlrow ,olleH\n\n")).toBe("ðŸ˜€")
	expect(iter.ltr.rune(" !dlrow ,olleH\n\n")).toBe(" ")
	expect(iter.ltr.rune("!dlrow ,olleH\n\n")).toBe("!")
	expect(iter.ltr.rune("dlrow ,olleH\n\n")).toBe("d")
	expect(iter.ltr.rune(" ,olleH\n\n")).toBe(" ")
	expect(iter.ltr.rune(",olleH\n\n")).toBe(",")
	expect(iter.ltr.rune("olleH\n\n")).toBe("o")
	expect(iter.ltr.rune("\n\n")).toBe("\n")
	expect(iter.ltr.rune("\n")).toBe("\n")
	expect(iter.ltr.rune("")).toBe("")
})

