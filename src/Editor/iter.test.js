import * as iter from "./iter"

test("backwards-rune", () => {
	expect(iter.rtl.rune("")).toBe("")
	expect(iter.rtl.rune("\n")).toBe("\n")
	expect(iter.rtl.rune("\n\n")).toBe("\n")
	expect(iter.rtl.rune("\n\nHello")).toBe("o")
	expect(iter.rtl.rune("\n\nHello,")).toBe(",")
	expect(iter.rtl.rune("\n\nHello, ")).toBe(" ")
	expect(iter.rtl.rune("\n\nHello, world")).toBe("d")
	expect(iter.rtl.rune("\n\nHello, world!")).toBe("!")
	expect(iter.rtl.rune("\n\nHello, world! ")).toBe(" ")
	expect(iter.rtl.rune("\n\nHello, world! ðŸ˜€")).toBe("ðŸ˜€")
	expect(iter.rtl.rune("\n\nHello, world! ðŸ˜€\n")).toBe("\n")
	expect(iter.rtl.rune("\n\nHello, world! ðŸ˜€\n\n")).toBe("\n")
})

