test("forwards-rune", () => {
	expect(iter.ltr.rune("\n\nðŸ˜€ !dlrow ,olleH")).toBe("\n")
	expect(iter.ltr.rune("\nðŸ˜€ !dlrow ,olleH")).toBe("\n")
	expect(iter.ltr.rune("ðŸ˜€ !dlrow ,olleH")).toBe("ðŸ˜€")
	expect(iter.ltr.rune(" !dlrow ,olleH")).toBe(" ")
	expect(iter.ltr.rune("!dlrow ,olleH")).toBe("!")
	expect(iter.ltr.rune("dlrow ,olleH")).toBe("d")
	expect(iter.ltr.rune(" ,olleH")).toBe(" ")
	expect(iter.ltr.rune(",olleH")).toBe(",")
	expect(iter.ltr.rune("olleH")).toBe("o")
	expect(iter.ltr.rune("\n\n")).toBe("\n")
	expect(iter.ltr.rune("\n")).toBe("\n")
	expect(iter.ltr.rune("")).toBe("")
})

