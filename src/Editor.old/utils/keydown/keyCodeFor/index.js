// Returns the key code for an en-US keyboard key.
function keyCodeFor(key) {
	const keyCode = keyMap[key]
	if (keyCode === undefined) {
		throw new Error(`keyCodeFor: no such key; key=${key}`)
	}
	return keyCode
}

